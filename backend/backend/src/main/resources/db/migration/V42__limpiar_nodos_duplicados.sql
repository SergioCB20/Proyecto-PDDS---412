-- Elimina nodos logisticos duplicados (IATA + ICAO para mismo aeropuerto).
-- Se queda con codigos ICAO (SKBO, SPIM, SCEL) y elimina IATA (BOG, LIM, SCL).
-- MIA y GRU se conservan (no tienen equivalente ICAO en la base).

-- IDs de nodos IATA a eliminar
-- BOG = 00000000-0000-0000-0003-000000000003 → SKBO = 00000000-0000-0000-0000-000000000001
-- LIM = 00000000-0000-0000-0003-000000000001 → SPIM = 00000000-0000-0000-0000-000000000005
-- SCL = 00000000-0000-0000-0003-000000000005 → SCEL = 00000000-0000-0000-0000-000000000007

-- 1. Actualizar vuelos que referencian nodos IATA → nodos ICAO
UPDATE vuelos SET origen_id = '00000000-0000-0000-0000-000000000001'
WHERE origen_id = '00000000-0000-0000-0003-000000000003';

UPDATE vuelos SET destino_id = '00000000-0000-0000-0000-000000000001'
WHERE destino_id = '00000000-0000-0000-0003-000000000003';

UPDATE vuelos SET origen_id = '00000000-0000-0000-0000-000000000005'
WHERE origen_id = '00000000-0000-0000-0003-000000000001';

UPDATE vuelos SET destino_id = '00000000-0000-0000-0000-000000000005'
WHERE destino_id = '00000000-0000-0000-0003-000000000001';

UPDATE vuelos SET origen_id = '00000000-0000-0000-0000-000000000007'
WHERE origen_id = '00000000-0000-0000-0003-000000000005';

UPDATE vuelos SET destino_id = '00000000-0000-0000-0000-000000000007'
WHERE destino_id = '00000000-0000-0000-0003-000000000005';

-- 2. Eliminar nodos IATA duplicados
DELETE FROM nodos_logisticos WHERE codigo_iata IN ('BOG', 'LIM', 'SCL');
